package com.wudianyi.wb.hongmao.entity;

/**
 * 返佣最高和最低
 * 
 * @author rhett
 * 
 */
public class BackHighAndLow {
	// one or two
	private String level;
	private Double high;// 最高返佣
	private Double low;// 最低返佣

	public BackHighAndLow() {
		super();
	}

	public BackHighAndLow(String level, Double high, Double low) {
		super();
		this.level = level;
		this.high = high;
		this.low = low;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public Double getHigh() {
		return high;
	}

	public void setHigh(Double high) {
		this.high = high;
	}

	public Double getLow() {
		return low;
	}

	public void setLow(Double low) {
		this.low = low;
	}

}
