package com.wudianyi.wb.hongmao.entity;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "hm_webinfo")
public class Webinfo {
	private String id;
	private double yuanperyear;// 一年收费多少钱
	private double agentone;//记录的是小数点
	private double agenttwo;//记录的是小数点
	private double agentthree;//记录的是小数点
	private double agentback;//记录的是小数点

	@Id
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public double getYuanperyear() {
		return yuanperyear;
	}

	public void setYuanperyear(double yuanperyear) {
		this.yuanperyear = yuanperyear;
	}

	public double getAgentone() {
		return agentone;
	}

	public void setAgentone(double agentone) {
		this.agentone = agentone;
	}

	public double getAgenttwo() {
		return agenttwo;
	}

	public void setAgenttwo(double agenttwo) {
		this.agenttwo = agenttwo;
	}

	public double getAgentthree() {
		return agentthree;
	}

	public void setAgentthree(double agentthree) {
		this.agentthree = agentthree;
	}

	public double getAgentback() {
		return agentback;
	}

	public void setAgentback(double agentback) {
		this.agentback = agentback;
	}

}
